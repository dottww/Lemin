#include "libft.h"

char		*ft_strdup(const char *src)
{
	int		i;
	char	*dup;

	if (!src || !(dup = (char*)malloc(sizeof(char) * (ft_strlen(src) + 1))))
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
